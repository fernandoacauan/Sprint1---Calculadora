//-----------------------------------------------------------------------------
// Name: Add()
// Desc: Adiciona dois números inteiros.
//-----------------------------------------------------------------------------
function Add(iNumber, iNumberTwo)
{
    return iNumber + iNumberTwo;
}





//-----------------------------------------------------------------------------
// Name: Sub()
// Desc: Subtrai dois números inteiros.
//-----------------------------------------------------------------------------
function Sub(iNumber, iNumberTwo)
{
    return iNumber - iNumberTwo;
}


